import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler, LabelEncoder
from sklearn.metrics import accuracy_score, classification_report
from sklearn.ensemble import RandomForestClassifier
import joblib

import warnings
warnings.filterwarnings("ignore")

DATA_PATH = 'marketing_campaign_response_dataset/campaign_responses.csv'

print(f"Loading dataset from: {DATA_PATH}")
df = pd.read_csv(DATA_PATH)
print(f" dataframe head : {df.head()}")
print(f" dataframe shape : {df.shape}")
print(f" dataframe info : {df.info()}")

y = df['responded']
df.drop(['customer_id', 'responded'], axis=1, inplace=True)
x = df

for col in x.columns:
    if x[col].dtype == 'object':
        x[col] = LabelEncoder().fit_transform(x[col])
    else:
        x[col] = StandardScaler().fit_transform(x[col].values.reshape(-1, 1))

x_train, x_test, y_train, y_test = train_test_split(x, y, test_size=0.2, random_state=42)
print(x_train.shape)
print(x_test.shape)
print(y_train.shape)
print(y_test.shape)

le = LabelEncoder()
y_train_encoded = le.fit_transform(y_train)
y_test_encoded = le.transform(y_test)

model = RandomForestClassifier(
    n_estimators=100,
    max_depth=8,
    class_weight='balanced',
    random_state=42,
    n_jobs=-1
)
model.fit(x_train, y_train_encoded)

y_pred_encoded = model.predict(x_test)

accuracy = accuracy_score(y_test_encoded, y_pred_encoded)
report = classification_report(y_test_encoded, y_pred_encoded, target_names=le.classes_)

print(f"Accuracy: {accuracy}\n")
print("Classification Report:\n", report)

joblib.dump(model, "marketing_campaign_response_prediction_model.joblib")
print("Model saved as marketing_campaign_response_prediction_model.joblib")
